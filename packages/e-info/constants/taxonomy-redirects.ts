/**
 * 對相對路徑的 /tag/{名稱} 做百分比編碼；其餘（/category、/feature、/node、
 * 外部網址）維持原樣。
 */
function encodeDestination(destination: string): string {
  if (/^https?:\/\//.test(destination)) return destination
  if (destination.startsWith('/tag/')) {
    return '/tag/' + encodeURIComponent(destination.slice('/tag/'.length))
  }
  return destination
}

